package s0319;

public class IceCream {

	private String name;
	private int price;

	public void set(String name, int price) {

		this.name = name;
		this.price = price;
	}

	public String getName() {
		String s = name;
		return s;
	}

	public int getPrice() {
		int p = price;
		return p;
	}

}
